    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        
        String[] lines = jTextArea1.getText().split(";");
        String[][] numbers = new String[lines.length][lines[0].split(" ").length];
        
        String[] targets = jTextField2.getText().split(" ");
        
        double[][] d = new double[lines.length][lines[0].split(" ").length];
        double[] testY = new double[lines.length];
        
        int numberOfLine = 0;
        
        for (String line : lines) {
            
            numbers[numberOfLine] = line.split(" ");
            
            int numberOfFeature = 0;
            
            for (String number : numbers[numberOfLine]) {
                
                d[numberOfLine][numberOfFeature] = Integer.parseInt(number.trim());
                numberOfFeature++;
                
            }
            
            numberOfLine++;
            
        }
        
        int numberOfTarget = 0;
        
        for (String target : targets) {
            
            testY[numberOfTarget] = Integer.parseInt(target.trim());
            numberOfTarget++;
            
        }
        
        double[][] data = Functions.scaler(d);
        double[] y = Functions.scaler(testY);
        
        Dense[] denses = {
      
            new Dense(data[0].length, "null"),
            new Dense(10, "softmax"),
            new Dense(5, "relu"),
            new Dense(3, "softmax"),
            new Dense(1, "sigmoid")
      
        };
        
        Sequential nn = new Sequential(denses);
        
        String[] metrics = {"accuracy"};
        nn.compile(0.1, "mse", metrics);
        
        double[][] toPredict = {{2, 2, 3}};
    
        double[] prediction = nn.predict(toPredict);
    
        for (double pr : prediction)
            jTextArea2.setText("Prediction: " + String.valueOf((int)(pr * Functions.yMax)));
        
    }                                        

